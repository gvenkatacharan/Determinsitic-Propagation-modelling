function [FX]=FresnelIntegral(X)
A=[1.595769140;
        -0.000001702;
        -6.808568854;
        -0.000576361;
        +6.920691902;
        -0.016898657;
        -3.050485660;
        -0.075752419;
        +0.850663781;
        -0.025639041;
        -0.150230960;
        +0.034404779];
    
    B=[-0.000000033;
        +4.255387524;
        -0.000092810;
        -7.780020400;
        -0.009520895;
        +5.075161298;
        -0.138341947;
        -1.363729124;
        -0.403349276;
        +0.702222016;
        -0.216195929;
        +0.019547031];
    
    C=[0.000000000;
        -0.024933975;
        +0.000003936;
        +0.005770956;
        +0.000689892;
        -0.009497136;
        +0.011948809;
        -0.006748873;
        +0.000246420;
        +0.002102967;
        -0.001217930;
        +0.000233939];
    
    E=[+0.199471140; 
        +0.000000023;
        -0.009351341;
        +0.000023006;
        +0.004851466;
        +0.001903218;
        -0.017122914;
        +0.029064067;
        -0.027928955;
        +0.016497308;
        -0.005598515;
        +0.000838386];

p=1.25331413732;
frnlmt=0.62665708;
FC=[];
FS=[];
FCS=[];
u=X;
su=sin(u);
cu=cos(u);

if u<=4;
    y=u*0.25;
    DS1=sqrt(y);
    y1A=A(6,1)+(A(7,1)+(A(8,1)+(A(9,1)+(A(10,1)+(A(11,1)+A(12,1)*y)*y)*y)*y)*y)*y;
    
    An=A(1,1)+(A(2,1)+(A(3,1)+(A(4,1)+(A(5,1)+y1A*y)*y)*y)*y)*y;
    
    y1B=B(6,1)+(B(7,1)+(B(8,1)+(B(9,1)+(B(10,1)+(B(11,1)+B(12,1)*y)*y)*y)*y)*y)*y;
    
    Bn=B(1,1)+(B(2,1)+(B(3,1)+(B(4,1)+(B(5,1)+y1B*y)*y)*y)*y)*y;
    
    DC=DS1*(cu*An+su*Bn);
    
    DS=DS1*(su*An-cu*Bn);
    
elseif u>4 && u<=40;
    
    y=4/u;
    DS2=sqrt(y);
    
    y1C=C(6,1)+(C(7,1)+(C(8,1)+(C(9,1)+(C(10,1)+(C(11,1)+C(12,1)*y)*y)*y)*y)*y)*y;
    
    Cn=C(1,1)+(C(2,1)+(C(3,1)+(C(4,1)+(C(5,1)+y1C*y)*y)*y)*y)*y;
    
    y1E=E(6,1)+(E(7,1)+(E(8,1)+(E(9,1)+(E(10,1)+(E(11,1)+E(12,1)*y)*y)*y)*y)*y)*y;
    
    En=E(1,1)+(E(2,1)+(E(3,1)+(E(4,1)+(E(5,1)+y1E*y)*y)*y)*y)*y;
    
    DC=0.5+DS2*(cu*Cn+su*En);
    
    DS=0.5+DS2*(su*Cn-cu*En);
    
elseif u>40;
    DS3=sqrt(u);
    u2=u*u;
    y=0.3989423;
    y1=0.199470;
    DC=0.5+(y-0.3/u2)*su/DS3-(y1-0.78/u2)*cu/(u*DS3);
    
    DS=0.5-(y-0.3/u2)*cu/DS3-(y1-0.748/u2)*su/(u*DS3);
    
end
% if xL<0;
%     
% fc=-DC*p;
% fs=-DS*p;
% else xL>=0;
    fc=DC*p;
    fs=DS*p;
% end

FX=frnlmt-fc-j*(frnlmt-fs);